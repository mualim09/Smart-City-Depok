     */
public function store(Request $request)
{
    $this->validate($request, [
    'judul'         => 'required',
    'pengirim'      => 'required',
    'deskripsi'     => 'required'
        ]);
     $broad = new Broadcast;
     $broad->judul     = $request->input('judul');
     $broad->pengirim     = $request->input('pengirim');
     $broad->deskripsi = $request->input('deskripsi');
     $broad->save();
    return redirect('/inputdataumum')->with('success', 'Broadcast telah dibuat');
}

